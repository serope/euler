/***********************************************************************
 * Project Euler (https://serope.com/github/euler.html)
 * Problem 16
 **********************************************************************/
#include "euler_bignum.h"

int main() {
	/*******************************************************************
	 * 1. Multiply 2 by itself one thousand times
	 ******************************************************************/
	big x = big_new("2");
	big_pow(x, 1000);

	
	/*******************************************************************
	 * 2. Sum the digits
	 ******************************************************************/
	int sum = big_sum_of_digits(x);
	
	
	/*******************************************************************
	 * 3. Print, cleanup, and exit
	 ******************************************************************/
	big_print(x);
	printf("%d \n", sum);
	BIG_FREE(x);
	return 0;
}
